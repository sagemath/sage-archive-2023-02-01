            q^5 + O(q^13),
            q^6 + O(q^13),
            q^7 + O(q^13),
            q^8 + O(q^13),
            q^9 + O(q^13),
            q^10 + O(q^13),
            q^11 + O(q^13),
            q^12 + O(q^13),
            O(q^13),
